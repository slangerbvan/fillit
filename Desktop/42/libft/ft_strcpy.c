#include "libft.h"

char	*ft_strcpy(char *dst, char *src)
{
	int i;

	i = 0;
	if (src == 0)
		return (NULL);
	while (src[i])
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
